    private void kitapEkleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kitapEkleActionPerformed
        String kitap_adi=kitap_adi_alani.getText();
        String yazar_adi=yazar_adi_alani.getText();
        String yayin_evi=yayin_evi_alani.getText();
        String kitap_turu=cb_kitap_turu.getSelectedItem().toString();
        String sayfasayisi=sayfa_sayisi_alani.getText();
        String stok=stok_alani.getText();
        if(kitap_adi.trim().equals("")||yazar_adi.trim().equals("")||yayin_evi.trim().equals("")||sayfasayisi.trim().equals("")||stok.trim().equals("")){
            kitap_mesaj_yazisi.setText("Lütfen bilgileri eksiksiz doldurun.");
        }
        else{
            islemler.kitapEkle(kitap_adi, yazar_adi, yayin_evi, kitap_turu, Integer.valueOf(sayfasayisi), Integer.valueOf(stok));
            kitaplariGoruntule();
            kitap_mesaj_yazisi.setText("Yeni Kitap başarıyla eklendi.");
        }
        
    }//GEN-LAST:event_kitapEkleActionPerformed

