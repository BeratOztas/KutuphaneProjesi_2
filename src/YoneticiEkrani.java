    public void uyeleriGoruntule(){
       model1.setRowCount(0);
        ArrayList<Uye> uye_listesi=new ArrayList<Uye>();
        uye_listesi=islemler.uyeleriGetir();
        if(uye_listesi != null){
        for(Uye uye:uye_listesi){
            Object[]eklenecek={uye.getTcKimlikNo(),uye.getAd(),uye.getSoyad(),uye.getEmail(),uye.getTelefon()};
            model1.addRow(eklenecek);
            
        }
        }
        
        
    }
    public void kitaplariGoruntule(){
        model2.setRowCount(0);
        ArrayList<Kitap> kitap_listesi=new ArrayList<Kitap>();
        kitap_listesi=islemler.kitaplariGetir();
        if(kitap_listesi != null){
            for(Kitap kitap:kitap_listesi){
                Object [] eklenecek={kitap.getId(),kitap.getAd(),kitap.getYazarAdi(),kitap.getYayinEvi(),kitap.getKitapTuru(),kitap.getSayfaSayisi(),kitap.getStok()};
                model2.addRow(eklenecek);
            }
        }
        
    }
